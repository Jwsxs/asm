	.DATA
EVEN_MSG:	.ASCII "EVEN\n"
EVEN_MSG_LEN = . - EVEN_MSG
ODD_MSG:	.ASCII "ODD\n"
ODD_MSG_LEN = . - ODD_MSG

ARRAY_LEN:	.QUAD 5
	
	.TEXT
	.GLOBL _start

_start:
	PUSH %RBP
	MOVQ %RSP, %RBP

	SUBQ (ARRAY_LEN), %RSP # REMOVE 132 BYTES FROM %RSP + 8 FOR THE ADDRESS
	MOVL $12, 0(%RSP)
	MOVL $7, 4(%RSP)
	
	XOR %RDI, %RDI # THE I VAR IN A FOR LOOP
	XOR %R8, %R8 # SAVE %RDI VALUE FOR LATER PRINTING OF THE RESULT
	
READ_ARRAY:
	MOV %RDI, %R8
	TESTB $1, (%RSP, %RDI, 4)
	JZ EVEN

ODD: # IMPAR
	MOV $1, %RAX
	MOV $1, %RDI
	MOV $ODD_MSG, %RSI
	MOV $ODD_MSG_LEN, %RDX
	SYSCALL
	JMP .L1

EVEN: # PAR
	MOV $1, %RAX
	MOV $1, %RDI
	MOV $EVEN_MSG, %RSI
	MOV $EVEN_MSG_LEN, %RDX
	SYSCALL

.L1:
	MOV %R8, %RDI
	INC %RDI
	CMP (ARRAY_LEN), %RDI
	JL READ_ARRAY
	
DONE:
	MOVQ $0X3C, %RAX
	XOR %RDI, %RDI
	SYSCALL
