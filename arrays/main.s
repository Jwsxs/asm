	.DATA
INIT_VAL:	.WORD 4
DONE_MSG:	.ASCII "ARRAY CREATED!\n"
MSG_LENGTH = . - DONE_MSG
	
	.TEXT
	.GLOBL _start

_start:
	PUSH %RBP
	MOVQ %RSP, %RBP

#	LEAL INIT_VAL, %EBP # LOAD EFFICIENT MEMORY ADDRESS TO 32-BIT BASE POINTER
#	MOV (%EBP), %BX # MOV IT'S LITERAL VALUE
#	MOVW [INIT_VAL], %CX

#	MOV $255, -0X4(%RBP) # MOV $15 TO 4 BYTES BEFORE %RBP -> CURRENTLY MARKING INIT_VAL ADDRESS
#	LEAL -0X4(%RBP), %EBP # LOAD IT'S ADDRESS TO 32-BIT BASE POINTER
#	MOV (%EBP), %CX # LOAD THE VALUE ON THE POINTED ADDRESS TO %CX

	SUB $0X20, %RBP # SET BASE POINT 32 BITS BACKWARDS - I BELIEVE
	MOV $0, %RAX
	MOV $5, %RSI
	CALL CREATE_ARRAY
	
	MOV $0X3C, %RAX # $60 OR $0X3C - THE SAME
	XOR %RDI, %RDI
	SYSCALL
	
CREATE_ARRAY:
#	%RSI 1ST ARG
	#	%RDI 2ND ARG
	# THIS IS THE WAY TO GET THE ADDRESS OF THE RESPECTIVE MEMORY WE'RE WRITING TO
	#    BASE  ELEM SIZE
#	LEA (%RBP, %RAX, 4), %RBX
	MOV %RAX, (%RBP, %RAX, 4) # MOV RBX, RBP[RAX * 4] - INTEL SYNTAX - FOLLOWING THE ARC. OF 80386 BOOK
	
	INC %RAX
	CMP %RAX, %RSI
	JGE CREATE_ARRAY
	CALL DONE
	
DONE:
	MOV $1, %RAX
	MOV $1, %RDI
	MOV $DONE_MSG, %RSI
	MOV $MSG_LENGTH, %RDX
	SYSCALL
	
	ADD $0X20, %RBP # CLEAN STACK
	RET
